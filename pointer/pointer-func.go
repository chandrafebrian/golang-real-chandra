package main

import "fmt"

type Adress struct {
	City, Province, Country string
}

// tanda bintang akan menuju reference ke *Adress struct agar bisa berubah country field nya dengan yg baru
func ChangeAddress(newAddress *Adress) {
	newAddress.Country = "amerika"

}

// lalu memanggil func ChangeAddress(dengan tanda dan &oldAddress) sebagai penunjuk perubahan data nya di kirim ke var tersebut
func main() {
	oldAddress := Adress{"subang", "jawabarat", "indonesia"}
	ChangeAddress(&oldAddress)
	fmt.Println(oldAddress)
}
